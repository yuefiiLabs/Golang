package main

import "fmt"

func main() {
	// Integer
	var a int = 10
	var b int64 = 20

	// Unsigned Integer
	var c uint = 30

	// Floating Point
	var d float32 = 3.14

	// Bilangan Kompleks
	var e complex64 = 1 + 2i

	fmt.Println("Integer:", a, b)
	fmt.Println("Unsigned Integer:", c)
	fmt.Println("Floating Point:", d)
	fmt.Println("Bilangan Kompleks:", e)
}
